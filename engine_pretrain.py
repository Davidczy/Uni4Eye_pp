def image_entropy_rgb(img):
    result = 0
    img = img.numpy()
    C, H, W = img.shape
    for k in range(3):
        hist, bin_edges = np.histogram(img[k,:,:], bins=np.arange(257), density=True)
        for i in np.arange(256):
            if hist[i] == 0:
                result = result
            else:
                result = result - hist[i] * (math.log(hist[i]) / math.log(2.0))

    return result/C

def image_entropy_3d(img):
    result = 0
    img = img.numpy()
    C, H, W, D = img.shape
    for k in range(C):
        hist, bin_edges = np.histogram(img[k,:,:,:], bins=np.arange(257), density=True)
        for i in np.arange(256):
            if hist[i] == 0:
                result = result
            else:
                result = result - hist[i] * (math.log(hist[i]) / math.log(2.0))

    return result/C

def normalization(data):
    _range = np.max(data) - np.min(data)
    return (data - np.min(data)) / _range



